import mongoose from 'mongoose';

const userAgentSessionSchema = new mongoose.Schema({
    title: {
        type: String,
        required: false
    },
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
}, {
    timestamps: {
        createdAt: 'created_at',
        updatedAt: 'updated_at'
    }
});

export default mongoose.model('UserAgentSession', userAgentSessionSchema);